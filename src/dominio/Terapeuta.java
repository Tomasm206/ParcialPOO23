package dominio;

public class Terapeuta {
    private String nombre;
    private int tarifa;

    public String getNombre() {
        return nombre;
    }

    public int getTarifa() {
        return tarifa;
    }

    public Terapeuta(String nombre, int tarifa) {
        this.nombre = nombre;
        this.tarifa = tarifa;

    }

}






